/**
 * ReplyContext：入站 reply / 引用消息上下文。
 *
 * 关键点（中文）
 * - 统一描述“当前消息所引用的上一条消息”。
 * - 供 Telegram / Feishu / 未来其他渠道复用。
 * - 该结构只表达模型真正需要的引用信息，不绑定具体平台原始 payload。
 */

/**
 * 归一化后的入站引用上下文。
 */
export interface InboundReplyContext {
  /**
   * 被引用消息的消息 ID。
   *
   * 说明（中文）
   * - 该值来自平台原始 reply 目标。
   * - 主要用于审计与调试，模型通常不会直接依赖它做推理。
   */
  messageId?: string;

  /**
   * 被引用消息发送者的展示名。
   *
   * 说明（中文）
   * - 若平台未提供姓名，则允许为空。
   * - 该字段用于帮助模型理解“这条被引用消息是谁说的”。
   */
  actorName?: string;

  /**
   * 被引用消息的归一化正文。
   *
   * 说明（中文）
   * - 文本消息时保存原始正文。
   * - 附件或无法直接展开的消息，会保存稳定的占位描述。
   */
  text?: string;

  /**
   * 用户显式框选的引用片段。
   *
   * 说明（中文）
   * - 仅部分平台支持“回复时引用一小段内容”。
   * - 若存在，该字段优先表达用户本次真正关注的局部内容。
   */
  quoteText?: string;
}
