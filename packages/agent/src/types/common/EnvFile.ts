/**
 * Env 文件工具相关类型定义。
 *
 * 职责说明（中文）
 * - 统一描述 `.env` / `.env.example` 写入工具使用的最小数据结构。
 * - 避免工具模块直接内联匿名对象，便于后续在其他初始化流程中复用。
 * - 这里只描述“文件写入条目”，不承载运行时环境变量快照语义。
 */

/**
 * Env 文件中的单条键值项。
 */
export interface EnvFileEntry {
  /**
   * 环境变量键名。
   *
   * 关键点（中文）
   * - 调用方应传入已规范化的变量名。
   * - 工具层不会自动把任意字符串修正成合法变量名。
   */
  key: string;

  /**
   * 环境变量值。
   *
   * 关键点（中文）
   * - 会按字面量写入目标文件。
   * - 当前工具不额外做引号包裹或 shell 转义。
   */
  value: string;
}
