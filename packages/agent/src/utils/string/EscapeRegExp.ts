/**
 * 正则转义工具模块。
 *
 * 职责说明（中文）
 * - 提供把普通字符串安全嵌入 `RegExp` 模式中的基础能力。
 * - 避免各业务模块手写重复实现，并降低正则特殊字符导致的误匹配风险。
 * - 这里只做字面量转义，不负责拼装完整正则表达式。
 */

/**
 * 转义字符串中的正则特殊字符。
 *
 * 关键点（中文）
 * - 返回值适合直接插入 `new RegExp(...)` 的模式字符串。
 * - 不会自动添加边界、分组或 flags。
 */
export function escapeRegExp(input: string): string {
  return String(input || "").replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
